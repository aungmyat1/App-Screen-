import axios from 'axios';

// Create axios instance with default config
const api = axios.create({
  baseURL: 'http://localhost:5000/api',
  headers: {
    'Content-Type': 'application/json',
  },
});

// Add a request interceptor to include auth token
api.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => {
    return Promise.reject(error);
  }
);

// Add a response interceptor to handle errors
api.interceptors.response.use(
  (response) => {
    return response;
  },
  (error) => {
    if (error.response?.status === 401) {
      // Handle unauthorized access
      localStorage.removeItem('token');
      localStorage.removeItem('user');
      window.location.href = '/login';
    }
    return Promise.reject(error);
  }
);

// Auth endpoints
export const authAPI = {
  register: (userData) => api.post('/auth/register', userData),
  login: (credentials) => api.post('/auth/login', credentials),
  getProfile: () => api.get('/auth/profile'),
};

// Screenshot endpoints
export const screenshotAPI = {
  requestScreenshots: (data) => api.post('/screenshots', data),
  getJobStatus: (id) => api.get(`/screenshots/${id}`),
  getUserJobs: () => api.get('/screenshots'),
};

// Payment endpoints
export const paymentAPI = {
  getClientToken: () => api.get('/payment/client-token'),
  processSubscription: (data) => api.post('/payment/subscribe', data),
  cancelSubscription: () => api.post('/payment/cancel'),
};

export default api;